#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    char s;
    int N;
    cin >> s >> N;

    for (int i = 1; i <= N; i++)
    {
        int M;
        cin >> M;

        for (int j = 1; j <= M; j++)
        {
            cout << s << "";
        }
        cout << endl;
    }

    return 0;
}
